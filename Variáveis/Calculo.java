public class Calculo {
    public static void main(String[] args) {

        int num1 = 50; //Atribui 50 a variável num1
        int num2 = 25; //Atribui 25 a variável num2

        int num3 = num1 - num2; //Atribui a subtração de num1 por num2 a variável num3
        int num4 = num1 + num1; //Atribui a soma de num1 por num1 a variável num4
        int num5 = num1 * num2; //Atribui a multiplicação de num1 por num2 a variável num5
        int num6 = num1 / num2; //Atribui a divisão de num1 por num2 a variável num6


        //Imprime as respectivas variáveis
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(num6);

    }
}
