public class Variavel {
    public static void main(String[] args) {
        int numero = 5; //Variável que armazena inteiros
        String palavra = "Rato"; //Variável que armazena palavras (sequência de caractéres)
        float z = 2.6f; //Variável que armazena números decimais
        double z2 = 2.8; //Variável que armazena números decimais
        char letra = 'A'; //Variável que armazena um único caracter
        long numeroGrande = 123187672; //Variável que armazena valores grandes
        boolean status = true; //Variável que armazena verdadeiro ou falso

        //Imrpime o texto e os valores das variáveis respectivas
        System.out.println("Variável inteira: " + numero); 
        System.out.println("Variável string: " + palavra);
        System.out.println("Variável float: " + z);
        System.out.println("Variável double: " + z2);
        System.out.println("Variável char: " + letra);
        System.out.println("Variável long: " + numeroGrande);
        System.out.println("Variável boolean: " + status);

    }
}
